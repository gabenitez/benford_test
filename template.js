//Receives an array containing the percentages for the digits and prints the corresponding digits
//Example: [0, 0, 0, 0, 0, 0, 0, 0, 0]
//First position is the percentage of digit 1, second is the percentage of digit 2, etc.
function plotPercentajesForNumbers (percentagesData) {
    let resGraph = [...dataTemplate];
    for (let i = 0; i < percentagesData.length; i++) {
        let percentage = percentagesData[i];
        let digit = i + 1;
        
        let percentageCounter = 0;
        for (let j = resGraph.length-3; j >= 0 ; j--) {
            percentageCounter++;
            if(percentageCounter >= percentage) {
                resGraph[j] = resGraph[j].replace (`##${digit}##`, `     `);             
            }
        }
    }   
    console.clear();
    for (let i = 0; i < resGraph.length; i++) {
        console.log(resGraph[i]);
    }
}


